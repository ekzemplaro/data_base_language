import	redis.clients.jedis.Jedis
// ------------------------------------------------------------------    
class redis_delete
{

// ------------------------------------------------------------------    
static void main(String[] args)  throws Exception
{
	println ("*** 開始 ***")

	String	key = args[0]
	println ("\tkey = " + key)
        
	Jedis jedis = new Jedis("host_dbase",6379)            

	jedis.del (key)           

	println ("*** 終了 ***")        
}

// ------------------------------------------------------------------    
}
