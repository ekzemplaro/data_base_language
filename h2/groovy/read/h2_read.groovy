import groovy.sql.Sql

import sql_manipulate
// ---------------------------------------------------------------------
class h2_read
{
// -------------------------------------------------------------------
static void main (args)
{
	println ("*** 開始 ***")

	def database = "/var/tmp/h2/cities"

	def sql = Sql.newInstance \
		("jdbc:h2:file:" + database, "sa", "", "org.h2.Driver")

	sql_manipulate.display_proc (sql)

	sql.close ()

	println ("*** 終了 ***")
}

