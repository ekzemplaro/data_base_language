line_out_proc(Data_in) :-
	concat_atom(Data_in,'\t',Str_aa),
	concat_atom([Str_aa,'\n'],Line_out),
	format(Line_out).
/* ------------------------------------------------------------------- */
data_out_proc(File_out,Data_a) :-
	tell(File_out),
	maplist(line_out_proc,Data_a),
	told.
